/**
 * 詳細な業種名 → 9カテゴリへ正規化
 *   飲食   ... 飲食店、 食堂、 レストラン、 喫茶等
 *   製造   ... 〜製造業 全般
 *   小売   ... 〜小売業 全般
 *   宿泊   ... ホテル、 旅館、 民宿等
 *   建設   ... 工事業、 建設業、 建築業 (建築材料 卸売は除く)
 *   農業   ... 農業 / 林業 / 漁業 / 畜産 等
 *   介護   ... 介護 / 福祉 / デイサービス / 老人ホーム 等
 *   運送   ... 運送 / 運輸 / 物流 / 倉庫 / トラック / タクシー 等
 *   その他 ... 上記以外
 *
 *   優先順位: 介護 > 宿泊 > 飲食 > 建設 > 運送 > 農業 > 小売 > 製造 > その他
 *     (より specific なものを優先、 「小売」「製造」 は最後にチェック)
 */

const INDUSTRY_CATEGORIES = ['飲食', '製造', '小売', '宿泊', '建設', '農業', '介護', '運送', 'その他'];

function normalizeIndustry(raw) {
  if (raw === null || raw === undefined) return 'その他';
  const s = String(raw);
  if (!s.trim()) return 'その他';

  // 介護 / 福祉 (デイサービス / 老人ホーム 等の高齢者福祉も含む)
  if (/介護|福祉|デイサービス|老人ホーム|養護|障害者|療養|訪問看護/.test(s)) return '介護';
  // 宿泊
  if (/宿泊|ホテル|旅館|民宿/.test(s)) return '宿泊';
  // 飲食店 (食堂・レストラン・喫茶) — 「飲食料品小売」 は別判定
  if (/飲食店|食堂|レストラン|喫茶|バー|居酒屋|カフェ/.test(s)) return '飲食';
  // 建設 (工事業 / 建設業 / 建築業 / 住まい・住宅・電気工事)
  //   ただし 建築材料卸売 や 建築事務所 は除外
  if (/工事業|建設業|建築業|住まい|住宅|電気工事/.test(s)) return '建設';
  // 運送 / 運輸 / 物流 / 倉庫
  if (/運送|運輸|物流|倉庫|トラック|タクシー|配送|郵便/.test(s)) return '運送';
  // 農業 / 林業 / 漁業 / 畜産
  if (/農業|林業|漁業|畜産|農家|養鶏|養豚/.test(s)) return '農業';
  // 小売
  if (/小売/.test(s)) return '小売';
  // 製造
  if (/製造/.test(s)) return '製造';
  return 'その他';
}

module.exports = {
  normalizeIndustry,
  INDUSTRY_CATEGORIES,
};
