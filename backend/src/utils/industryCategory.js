/**
 * raw: 自由文字列 (業種名 / 業種詳細 / 業種カンマ列 等)
 *   - 「ショッピング, コンビニエンスストア, 各種商品小売業, 」 → 小売
 *   - 「食料品製造業」  → 製造
 *   - 「縫製業」        → 製造
 *   - 「そば店」        → 飲食
 *   - 「コンビニエンスストア」 → 小売
 */
function normalizeIndustry(raw) {
  if (raw === null || raw === undefined) return 'その他';
  const s = String(raw);
  if (!s.trim()) return 'その他';

  if (RX.care.test(s))         return '介護';
  if (RX.lodging.test(s))      return '宿泊';
  if (RX.food.test(s))         return '飲食';
  if (RX.cleaning.test(s))     return '清掃';
  if (RX.construction.test(s)) return '建設';
  if (RX.transport.test(s))    return '運送';
  if (RX.agri.test(s))         return '農業';
  if (RX.retail.test(s))       return '小売';
  if (RX.manufacturing.test(s))return '製造';
  return 'その他';
}

module.exports = {
  normalizeIndustry,
  INDUSTRY_CATEGORIES,
};
